public class class5Ekspresije {
    public static void main(String[] args) {
        /* Prisecanje:
        ekspresije: operandi i operacije
        operandi: vrednosti, promenljive, brojevi...
        operacije: + - * / %
         */
        int friends = 10;
        friends = friends + 1; // ili - * / % 1 ...
        System.out.println(friends);
        friends++; // inkrement
        System.out.println(friends);
        friends--; // dikrement
        System.out.println(friends);
    }
}
