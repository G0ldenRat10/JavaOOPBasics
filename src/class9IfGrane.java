public class class9IfGrane {
    public static void main(String[] args) {
        // if-elif-else

        int age = 18;

        if(age>=18) {
            System.out.println("You have enough years to enter!");
        } else if(age<18) {
            System.out.println("You don't have enough years to enter!");
        } else {
            System.out.println("Invalid number entered.");
        }
    }
}
